/*
 * Track.c — TCRT5000 五路寻迹模块驱动
 *
 * 本模块负责：
 *   1. 初始化5路数字寻迹传感器的GPIO
 *   2. 提供读取传感器状态的接口
 *   3. 使用加权重心法计算黑线位置
 *
 * 硬件信息：
 *   - 模块：TCRT5000 五路红外寻迹模块（数字输出）
 *   - 输出逻辑：  黑线→LOW(0V)    白面→HIGH(3.3V)
 *   - 超出检测范围（传感器悬空）= LOW（同黑线电平）
 *   - 传感器物理间距（从左到右）：
 *       S1-S2: 22mm,  S2-S3: 24.5mm,  S3-S4: 24.5mm,  S4-S5: 22mm
 *   - 总宽度：93mm
 *
 *   IMPORTANT ── 超出检测范围问题：
 *     平衡车在运动时车体俯仰角会变化，安装在车头的寻迹模块
 *     离地高度也会随之波动。如果传感器离地太远（>15~20mm），
 *     红外反射光太弱，输出电平会变成 LOW（与黑线相同）。
 *     解决方式：
 *       ① 调整模块上的可调电位器，让它在适当高度能可靠区分黑白
 *       ② 安装时保证正常平衡姿态下传感器距地面 10~15mm
 *       ③ 在控制逻辑中增加角度判断：当车体倾斜过大时忽略寻迹
 */

#include "Track.h"

/*──────────────────────────────────────────────────────────────────────
 *  硬件初始化
 *──────────────────────────────────────────────────────────────────────*/

/**
 * @brief  寻迹传感器 GPIO 初始化
 * 
 * 引脚配置详情：
 *   ┌──────────┬─────────┬─────────────────────────────────────┐
 *   │ 引脚     │ 方向    │ 说明                                │
 *   ├──────────┼─────────┼─────────────────────────────────────┤
 *   │ PA9      │ 输入    │ 原为 USART1_TX，现释放给寻迹 S1     │
 *   │ PA10     │ 输入    │ 原为 USART1_RX，现释放给寻迹 S2     │
 *   │ PA11     │ 输入    │ 空闲引脚，分配给寻迹 S3             │
 *   │ PA12     │ 输入    │ 空闲引脚，分配给寻迹 S4             │
 *   │ PC15     │ 输入    │ 备份域引脚，需特殊处理（见下方）    │
 *   └──────────┴─────────┴─────────────────────────────────────┘
 * 
 * PC15 特殊处理说明：
 *   - PC15 属于 备份域（Backup Domain） 引脚（与PC13、PC14同在）
 *   - 默认状态下，PC14/PC15 与 32.768kHz 晶振(LSE)相关联
 *   - 若要作为普通GPIO使用，必须：
 *     ① 使能 PWR 和 BKP 外设时钟
 *     ② 使能备份域访问（PWR_BackupAccessCmd）
 *     ③ 确认 LSE 振荡器已关闭
 *     ④ 禁用 RTC 输出和侵入检测功能
 *   - 板上有32.768kHz晶振焊着但本工程没有使能LSE，
 *     所以使用 PC15 作为 GPIO 完全没问题。
 */
void Track_Init(void)
{
    /* ── 第1步：使能 GPIO 时钟 ── */
    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);
    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC, ENABLE);

    /* ── 第2步：PC15 备份域特殊处理 ── */
    /*
     * 备份域手册摘要（RM0008 Rev18, Section 6.2）：
     *   在复位后，PC14和PC15被配置为LSE振荡器引脚。
     *   要使用它们作为GPIO，需通过RCC_BDCR寄存器关闭LSE并使能
     *   相应配置。为此必须先使能PWR和BKP时钟并解锁备份域写保护。
     */
    RCC_APB1PeriphClockCmd(RCC_APB1Periph_PWR | RCC_APB1Periph_BKP, ENABLE);
    PWR_BackupAccessCmd(ENABLE);            /* 解锁备份域写保护       */
    RCC_LSEConfig(RCC_LSE_OFF);             /* 强制关闭LSE振荡器      */
    BKP_TamperPinCmd(DISABLE);              /* 禁用PC13侵入检测       */
    BKP_RTCOutputConfig(BKP_RTCOutputSource_None);/* 禁止RTC时钟输出到PC13  */

    /* ── 第3步：配置 PA9~PA12 为上拉输入 ── */
    /*
     * 选择上拉输入的理由：
     *   传感器模块输出为开漏或推挽输出(取决于模块设计)，
     *   上拉输入可以确保引脚电平稳定，避免浮空导致的误读。
     *   当传感器输出高阻时（某些模块在未上电时），上拉电阻
     *   会确保引脚读为高电平，被视为"白色"。
     */
    GPIO_InitTypeDef GPIO_InitStructure;
    GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_IPU;   /* 上拉输入 */
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_InitStructure.GPIO_Pin   = GPIO_Pin_9 | GPIO_Pin_10
                                  | GPIO_Pin_11 | GPIO_Pin_12;
    GPIO_Init(GPIOA, &GPIO_InitStructure);

    /* ── 第4步：配置 PC15 为上拉输入 ── */
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_15;
    GPIO_Init(GPIOC, &GPIO_InitStructure);
}

/*──────────────────────────────────────────────────────────────────────
 *  单传感器读取
 *──────────────────────────────────────────────────────────────────────
 * TCRT5000 数字输出电平映射：
 *       引脚状态     │    物理含义     │  函数返回值
 *   ────────────────┼─────────────────┼────────────
 *   低电平 (RESET)  │ 检测到黑线       │ 1
 *   高电平 (SET)    │ 未检测(白色)    │ 0
 *   低电平 (RESET)  │ 超出检测范围    │ 1 (同黑线)
 *   ────────────────┴─────────────────┴────────────
 *
 * 实现方式：GPIO_ReadInputDataBit() 读取引脚电平，
 * 与 Bit_RESET(0) 比较，相等则返回1(黑线)。
 */

uint8_t Track_GetS1(void)
{
    return (GPIO_ReadInputDataBit(TRACK_S1_PORT, TRACK_S1_PIN) == Bit_RESET) ? 1 : 0;
}

uint8_t Track_GetS2(void)
{
    return (GPIO_ReadInputDataBit(TRACK_S2_PORT, TRACK_S2_PIN) == Bit_RESET) ? 1 : 0;
}

uint8_t Track_GetS3(void)
{
    return (GPIO_ReadInputDataBit(TRACK_S3_PORT, TRACK_S3_PIN) == Bit_RESET) ? 1 : 0;
}

uint8_t Track_GetS4(void)
{
    return (GPIO_ReadInputDataBit(TRACK_S4_PORT, TRACK_S4_PIN) == Bit_RESET) ? 1 : 0;
}

uint8_t Track_GetS5(void)
{
    return (GPIO_ReadInputDataBit(TRACK_S5_PORT, TRACK_S5_PIN) == Bit_RESET) ? 1 : 0;
}

/*──────────────────────────────────────────────────────────────────────
 *  全传感器读取 — 位掩码方式
 *──────────────────────────────────────────────────────────────────────
 * 返回值格式（16位无符号整数，低5位有效）：
 *   bit4   bit3   bit2   bit1   bit0
 *   (S5)   (S4)   (S3)   (S2)   (S1)
 *   PC15   PA12   PA11   PA10   PA9
 *
 * 例：仅中间传感器(S3)检测到黑线 → 返回值 = 0x04
 *     右侧两个传感器都检测到     → 返回值 = 0x18
 *     全部检测到                 → 返回值 = 0x1F
 */
uint16_t Track_Get(void)
{
    uint16_t val = 0;
    if (Track_GetS1()) val |= TRACK_MASK_S1;
    if (Track_GetS2()) val |= TRACK_MASK_S2;
    if (Track_GetS3()) val |= TRACK_MASK_S3;
    if (Track_GetS4()) val |= TRACK_MASK_S4;
    if (Track_GetS5()) val |= TRACK_MASK_S5;
    return val;
}

/*──────────────────────────────────────────────────────────────────────
 *  核心算法：加权重心法计算黑线位置
 *──────────────────────────────────────────────────────────────────────
 *
 *  原理：
 *    将五个传感器视为一维离散采样点，用"重心(质心)"公式计算
 *    黑线在传感器阵列上的等效横向位置。
 *
 *  为什么用这个算法？
 *    - 当黑线正好在两个传感器中间时（例如在S2和S3之间），
 *      两个传感器都会被触发一小部分区域 → 都能检测到黑线。
 *      重心法可以在这两个传感器之间"插值"，得到连续位置值，
 *      而不是离散的"在S2上"或"在S3上"。
 *    - 这样 PID 控制可以做出平滑的转向修正，避免左右抖动。
 *
 *  数学表达式：
 *                    Σ ( s[i] × p[i] )
 *      position = ──────────────────────
 *                      Σ s[i]
 *
 *    其中：
 *      s[i] ∈ {0, 1}   — 第i个传感器的检测结果(0=白色,1=黑线)
 *      p[i] (单位:mm)   — 第i个传感器相对于S3(中心)的物理位置
 *
 *  传感器坐标（基于实测物理间距）：
 *    ┌─────┬─────────┬──────────┬─────────────────────────┐
 *    │ 传感器 │ 引脚    │ 坐标(mm)  │ 计算依据                   │
 *    ├─────┼─────────┼──────────┼─────────────────────────┤
 *    │ S1  │ PA9     │ -46.5   │ -(22 + 24.5) ≈ 最左边  │
 *    │ S2  │ PA10    │ -24.5   │ -(24.5) ≈ 左中         │
 *    │ S3  │ PA11    │   0     │ 中心(参考原点)         │
 *    │ S4  │ PA12    │ +24.5   │ +(24.5) ≈ 右中         │
 *    │ S5  │ PC15    │ +46.5   │ +(22 + 24.5) ≈ 最右边  │
 *    └─────┴─────────┴──────────┴─────────────────────────┘
 *    说明：S1-S2=22mm, S2-S3=24.5mm, S3-S4=24.5mm, S4-S5=22mm
 *          总跨度 = 93mm
 *
 *  计算示例：
 *    ┌──────────────────────────────────┬───────────┬──────────────┐
 *    │ 传感器触发状态                   │ 位置(mm)  │ 对应情况     │
 *    ├──────────────────────────────────┼───────────┼──────────────┤
 *    │ ●: 黑线, ○: 白色                │           │              │
 *    │ S1 S2 S3 S4 S5                  │           │              │
 *    │ ○  ○  ●  ○  ○                  │   0.0     │ 线在正中间   │
 *    │ ○  ●  ●  ○  ○                  │ -12.25    │ 线略偏左     │
 *    │ ○  ○  ●  ●  ○                  │ +12.25    │ 线略偏右     │
 *    │ ●  ○  ○  ○  ○                  │ -46.5     │ 线在最左侧   │
 *    │ ○  ○  ○  ○  ●                  │ +46.5     │ 线在最右侧   │
 *    │ ●  ●  ●  ●  ●                  │   0.0     │ 全黑(弯道)   │
 *    │ ○  ○  ○  ○  ○                  │ 999.0     │ 未找到线     │
 *    └──────────────────────────────────┴───────────┴──────────────┘
 *
 *  坐标系约定：
 *    - 负值 → 黑线在车体左侧 → 应右转（增加右轮/减小左轮）     ← 见下方注释
 *    - 正值 → 黑线在车体右侧 → 应左转（增加左轮/减小右轮）
 *    ───────────────────────────────────────────────────────────────
 *    注：上述转向方向是"开环说法"。实际在PID平衡车中，
 *        该position值作为 TurnPID.Target，由转向PID闭环
 *        自动计算差速 PWM。
 *
 *  边缘情况处理：
 *    1. 全线检测(0x1F = 全部检测到黑线)
 *       → 通常出现在急弯处黑线较宽时，此时返回0.0(认为居中)
 *    2. 无检测(0x00 = 均未检测到)
 *       → 可能原因：①脱线 ②传感器离地过高 ③在黑线间隙处
 *         返回999.0f 供上层逻辑判断是否停止/减速/搜索
 *    3. 单边触发(如仅S1或仅S5)
 *       → 位置值达到±46.5mm极端值，通常意味着即将脱线，
 *         需要大幅转向或减速
 *
 *  ── 与PID控制器的集成 ──
 *    在 TIM1 中断（每50ms）中：
 *      float pos = Track_GetPosition();
 *      if (pos < 100.0f) {                     // 在线
 *          TurnPID.Target = pos * TrackKp;      // 位置×比例系数
 *          SpeedPID.Target = TrackSpeed;        // 恒速前进
 *      } else {                                 // 脱线
 *          SpeedPID.Target = TrackSpeed * 0.3f; // 减速找线
 *      }
 *    TrackKp 将位置(mm)转换为转向角度目标值，需在现场调试中确定。
 *──────────────────────────────────────────────────────────────────────*/

/**
 * @brief  加权重心法计算黑线位置
 * @retval 位置坐标（单位：mm）
 *         - 负值：黑线在车体左侧
 *         - 正值：黑线在车体右侧
 *         - 0.0f：线居中，或全部传感器均检测到黑线
 *         - 999.0f：没有任何传感器检测到黑线
 */
float Track_GetPosition(void)
{
    /* 读取5路传感器状态 */
    uint8_t s[5];
    s[0] = Track_GetS1();
    s[1] = Track_GetS2();
    s[2] = Track_GetS3();
    s[3] = Track_GetS4();
    s[4] = Track_GetS5();

    /*
     * 传感器物理坐标（单位：mm，以S3为原点）
     * 使用实际毫米值的好处：
     *   - 位置输出有明确的物理意义（"线偏右约12mm"）
     *   - TrackKp 可根据物理单位推算初始值
     *   - 不同车型/不同安装方式之间更容易移植
     *
     *   S1       S2       S3       S4       S5
     *   │        │        │        │        │
     * ──┤────────┤────────┤────────┤────────┤──    → X轴正方向
     *  -46.5   -24.5     0      +24.5    +46.5   (mm)
     */
    const float weights[5] = {-46.5f, -24.5f, 0.0f, 24.5f, 46.5f};

    float numerator   = 0.0f;   /* 分子：Σ(s[i] × w[i]) */
    float denominator = 0.0f;   /* 分母：Σ(s[i])        */
    uint8_t i;

    /* 累加计算重心 */
    for (i = 0; i < 5; i++)
    {
        if (s[i])
        {
            numerator   += weights[i];   /* s[i]=1, 直接累加权重 */
            denominator += 1.0f;          /* 参与计数的传感器数  */
        }
    }

    /* ── 边界情况处理 ── */

    /*
     * 情况1：没有传感器检测到黑线
     *   - 返回 999.0f 特殊值，上层逻辑据此决定减速/停止/搜索
     *   - 使用大值而非-1是因为-1在位置范围内有实际物理意义
     */
    if (denominator < 0.5f)
    {
        return 999.0f;
    }

    /*
     * 情况2：全部传感器都检测到黑线（5路全触发）
     *   - 常见于遇到粗线/转角/十字路口时
     *   - 此时重心必然在0处（对称），认为是居中，继续直行
     */
    if (denominator > 4.5f)
    {
        return 0.0f;
    }

    /* ── 正常情况：计算加权重心 ── */
    return numerator / denominator;
}
